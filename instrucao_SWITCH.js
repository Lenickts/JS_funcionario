var diaSem = 1;

switch (diaSem){
    case 1:
        console.log("Domingo");
        break;
    
    case 2:
        console.log("Segunda-feira");
        break;
    
    case 3:
        console.log("Terça-feira");
        break;

    case 4:
        console.log("Quarta-feira");
        break;
    default:
        console.log("Esse dia da semana não existe");
}
